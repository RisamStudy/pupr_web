<script>
function imagePreview() {
    return {
        imageUrl: "{{ $user->profile_photo_path ? asset($user->profile_photo_path) : asset('assets/img/avatar.jpg') }}",
        updatePreview() {
            const reader = new FileReader()
            reader.onload = (e) => {
                this.imageUrl = e.target.result
            }
            reader.readAsDataURL(this.$refs.fileInput.files[0])
        }
    }
}
</script>
